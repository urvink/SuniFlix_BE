package com.flix.suni.model;

public class TrailerLink {

		private String rel ;
		private String href;

		public Object getRel() {
				return rel;
		}

		public void setRel(String rel) {
				this.rel = rel;
		}

		public String getHref() {
				return href;
		}

		public void setHref(String href) {
				this.href = href;
		}
}