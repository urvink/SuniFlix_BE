package com.flix.suni.model;

public class Genre {
		private Integer id;
		private String genreName;

		public Integer getId() {
				return id;
		}

		public void setId(Integer id) {
				this.id = id;
		}

		public String getGenreName() {
				return genreName;
		}

		public void setGenreName(String genreName) {
				this.genreName = genreName;
		}
}
